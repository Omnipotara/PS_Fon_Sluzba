    private void popuniComboBoxProfesora() {
        cmbProfesori.removeAllItems();
        List<Profesor> listaProfesora = kontroler.uzmiListuProfesora();
        for(Profesor p : listaProfesora){
            cmbProfesori.addItem(p);
        }
    }

    private void osveziTabelu() {
        List<Predmet> listaPredmeta = new ArrayList<>();
        Profesor izabraniProfesor = (Profesor) cmbProfesori.getSelectedItem();
        listaPredmeta = kontroler.napuniTabeluPredmeta(izabraniProfesor);
        
        ModelTabelePredmeta mtp = new ModelTabelePredmeta(listaPredmeta);
        tblPredmeti.setModel(mtp);
    }
