    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int selectedRow = tblPredmeti.getSelectedRow();
        
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Molimo Vas izaberite neki predmet za brisanje.", "Izaberite predmet", JOptionPane.ERROR_MESSAGE);
        } else {
            ModelTabelePredmeta mtp = (ModelTabelePredmeta)tblPredmeti.getModel();
            int id = mtp.getListaPredmeta().get(selectedRow).getId();
            kontroler.obrisiPredmet(id);
            JOptionPane.showMessageDialog(this, "Brisanje predmeta je uspesno obavljeno!.", "Uspesna operacija brisanja", JOptionPane.INFORMATION_MESSAGE);
            osveziTabelu();
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

